package com.cpm.delegates;

public class TargetKpi {
	
	private String kpi;
	private String food;

	private String bev;

	public String getKpi() {
		return kpi;
	}

	public void setKpi(String kpi) {
		this.kpi = kpi;
	}

	public String getFood() {
		return food;
	}

	public void setFood(String food) {
		this.food = food;
	}

	public String getBev() {
		return bev;
	}

	public void setBev(String bev) {
		this.bev = bev;
	}
}
